function normalizeLanguageCode(value) {
  if (!value) return null;
  const code = String(value).toLowerCase().split(/[-_]/)[0];
  return Object.prototype.hasOwnProperty.call(SUPPORTED_LANGUAGES, code) ? code : null;
}

function getInitialLanguage() {
  const queryLang = normalizeLanguageCode(new URLSearchParams(window.location.search).get("lang"));

  let storedLang = null;
  try {
    storedLang = normalizeLanguageCode(window.localStorage.getItem(LANDING_STORAGE_KEY));
  } catch {
    storedLang = null;
  }

  const browserLang = [...(navigator.languages || []), navigator.language]
    .map(normalizeLanguageCode)
    .find(Boolean);

  return queryLang || storedLang || browserLang || DEFAULT_LANGUAGE;
}

function updateUrlLanguage(lang) {
  const url = new URL(window.location.href);
  if (lang === DEFAULT_LANGUAGE) {
    url.searchParams.delete("lang");
  } else {
    url.searchParams.set("lang", lang);
  }
  window.history.replaceState({ lang }, "", `${url.pathname}${url.search}${url.hash}`);
}

function getTranslation(lang, key) {
  return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS[DEFAULT_LANGUAGE]?.[key] ?? "";
}

function applyLanguage(lang) {
  const safeLang = normalizeLanguageCode(lang) || DEFAULT_LANGUAGE;
  const dir = SUPPORTED_LANGUAGES[safeLang]?.dir || "ltr";

  document.documentElement.lang = safeLang;
  document.documentElement.dir = dir;
  if (document.body) {
    document.body.dir = dir;
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getTranslation(safeLang, key);
    if (value !== "") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const mappings = element.dataset.i18nAttr.split(";");
    mappings.forEach((mapping) => {
      const [attributeName, key] = mapping.split(":").map((part) => part.trim());
      if (!attributeName || !key) return;
      const value = getTranslation(safeLang, key);
      if (value !== "") {
        element.setAttribute(attributeName, value);
      }
    });
  });

  const languageSelect = document.getElementById("landing-language-select");
  if (languageSelect) {
    languageSelect.value = safeLang;
  }
}

function initLandingTranslations() {
  const initialLanguage = getInitialLanguage();
  applyLanguage(initialLanguage);

  const languageSelect = document.getElementById("landing-language-select");
  if (!languageSelect) return;

  languageSelect.addEventListener("change", (event) => {
    const nextLanguage = normalizeLanguageCode(event.target.value) || DEFAULT_LANGUAGE;
    try {
      window.localStorage.setItem(LANDING_STORAGE_KEY, nextLanguage);
    } catch {
      // Ignore storage failures.
    }
    updateUrlLanguage(nextLanguage);
    applyLanguage(nextLanguage);
  });
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLandingTranslations, { once: true });
} else {
  initLandingTranslations();
}
